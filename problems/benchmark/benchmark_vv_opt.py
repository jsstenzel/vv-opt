def vv_eval_problem(problem):
	print(problem)
	res = 100
	thetas = np.arange(0,1,1/res)
	
	###plot output of H vs theta
	Qs = [problem.H([t]) for t in thetas]
	plt.plot(thetas, Qs)
	plt.xlabel("theta")
	plt.ylabel("Q")
	plt.title("Q = H(theta)")
	plt.show()
	
	###plot output of eta as heatmap on theta vs d
	ds = np.arange(0,1,1/res)
	#ys = [problem.eta(t, d, err=False) for t,d in join(thetas,ds)]
	ys = np.empty((res,res))
	for i_t, t in enumerate(thetas):
		for i_d, d in enumerate(ds):
			ys[i_t,i_d] = problem.eta([t], [d], err=False)[0]
	sns.heatmap(ys, cmap="inferno")
	plt.title("y = eta(theta,d)")
	plt.ylabel('theta')
	plt.xlabel('d')
	plt.show()

def vv_eval_theta(problem, theta):
	QoI_nominal = problem.H([theta], verbose=True)
	print("Given the theta:", theta)
	print("Q:", QoI_nominal)
	
	###plot value of eta on theta vs d
	ds = np.arange(0,1,.001)
	ys = [problem.eta([theta], [d], err=True)[0] for d in ds]
	ynoms = [problem.eta([theta], [d], err=False)[0] for d in ds]
	plt.plot(ds, ys, 'red')
	plt.plot(ds, ynoms)
	plt.xlabel("d")
	plt.ylabel("y")
	plt.title("y=eta(theta="+str(theta)+",d)")
	plt.show()

