def vv_UP_exp(problem, dd, theta_nominal, n=10**4, savefig=False):
	print("Likelihood distribution for","(theta="+str(theta_nominal[0]),"d="+str(dd[0])+" hr)","case:",flush=True)
	#tt = problem.prior_rvs(1); print(tt)
	tt = theta_nominal
