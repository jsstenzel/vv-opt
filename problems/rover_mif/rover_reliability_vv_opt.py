def vv_UA(problem, n):
	theta_prior_samples = problem.prior_rvs(n)
	print(theta_prior_samples)
	uncertainty_prop_plots(theta_prior_samples, xlabs=problem.theta_names, saveFig='', doLog=True)
	
def vv_UP(problem, n):
	theta_prior_samples = problem.prior_rvs(n)
	Qs = [problem.H(theta) for theta in theta_prior_samples]
	uncertainty_prop_plot(Qs, xlab="total downtime [hr]", saveFig='', bins=100, doLog=True)
	print("average downtime:",np.mean(Qs))
	print("there is a 95% probability that the downtime is less than",np.quantile(Qs,0.95))

